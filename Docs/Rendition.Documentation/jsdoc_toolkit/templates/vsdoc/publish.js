/* convertes words to UUIDs that can be used in AML files */
function generateAMLIDHASH(name) {
	if(name.indexOf('#')!==-1){
		name = name.substring(name.indexOf('#'));
	}
	var hash = hex_sha1(name).replace('1','');
	/*format like 06579D24-D0AA-4093-AC79-E420EC700B6A */
	var start = 5;
	var AMLID = hash.substring(start+0, start+8) + '-' +
	hash.substring(start+8, start+12) + '-' +
	hash.substring(start+12, start+16) + '-' +
	hash.substring(start+16, start+20) + '-' +
	hash.substring(start+20, start+32);
	return AMLID;
}
